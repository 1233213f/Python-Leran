from math import*
x,y=eval(input("Please input x,y:"))
print("x + y =",x+y," x - y =",x-y, " x * y = ",x*y, " x / y =",x/y)#四则运算
print("x // y =",x//y)#x/y商的最大整数
print("x % y =",x%y)#x/y之商的余数
print("x ** y =",x**y)#x的y次幂
#print("abs（x） =",abs（x）)#x的绝对值
#print("divmod（x，y） =",divmod（x，y）)#（x//y,x%y）
print("pow(x,y) =",pow(x,y))#x的y次幂
